
const сумма_кредита = 200000;
const процентная_ставка = 10;
const срок_кредита = 2;
const общая_сумма_выплаты = сумма_кредита * (1 + (процентная_ставка / 100) * срок_кредита);
const переплата = общая_сумма_выплаты - сумма_кредита;
console.log("Общая сумма выплаты: ", общая_сумма_выплаты, " руб.");
console.log("Переплата: ", переплата, " руб.");
